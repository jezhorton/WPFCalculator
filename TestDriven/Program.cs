using System;
using System.Collections.Generic;
namespace TestDriven
{
    class Program
    {
        static void Main(string[] args)
        {
            Console.WriteLine("Hello World!");
            /*
             *Test Driven Development Steps
             * 1.Get the requirement from the user (what app to build)
             * 2.Give the requirement to the architect designer for review
             * 3.Recieve specification from analysts to developer
             * 4.For each item on the requirement specification determine the proposed function
             * 5.For each function we can calculate the expected input and output
             * 6.From this input and output we can write some test cases which expect this output given
             * 7.We add these tests to our application and run them
             * 8.The tests will fail
             * 9.We write the minimum code for the tests to pass
             * 10.Once the tests pass we are done
             * 11.Refactor the code to make it more robust and efficient
             * 12.Repeat until all items in requirements specifications are met
             */
        }
    }
}
